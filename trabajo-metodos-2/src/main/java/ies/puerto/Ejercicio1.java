package ies.puerto;

public class Ejercicio1 {

    /**
     * Funcion que calcula el suelo segun las horas trabajadas
     * @param numeroHoras trabajadas
     * @return sueldo
     */
    public float calcularSueldoHoras(float numeroHoras) {
        float sueldo;
        sueldo = numeroHoras*10;
        return sueldo;
    }

}